CREATE VIEW [history].[STUDENT_TERMS__STTR_STATUSES] AS
SELECT [STTR.STUDENT], [STTR.TERM], [STTR.ACAD.LEVEL]
       
, CAST(LTRIM(RTRIM(CA1.Item)) AS VARCHAR(30)) AS [STTR.STATUS]
     , CA1.ItemNumber AS ItemNumber
     , EffectiveDatetime
  FROM [history].[STUDENT_TERMS]
  
 CROSS APPLY dbo.DelimitedSplit8K([STTR.STATUS],', ') CA1
 WHERE COALESCE([STTR.STATUS],'') != ''
       
GO
